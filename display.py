#Functions
def ActivateLCD():
    wiringpi.digitalWrite(pin_CS_lcd, 0)       # Actived LCD using CS
    time.sleep(0.000005)

def DeactivateLCD():
    wiringpi.digitalWrite(pin_CS_lcd, 1)       # Deactived LCD using CS
    time.sleep(0.000005)

def ChooseFace(light_amount, temperature):
    if optimal_temperature_min <= temperature <= optimal_temperature_max and optimal_light_min <= light_amount <= optimal_light_max:
        face = smile
    else:
        face = sad
    return face

def StopDisplay():
    lcd_1.clear()
    lcd_1.refresh()
    lcd_1.set_backlight(0)
    DeactivateLCD()


def ChangeDisplay(light, temp):
    # Display text
    lcd_1.clear()
    ActivateLCD()
    lcd_1.set_backlight(1)
    lcd_1.put_string(f"Light:\n{light} lux\nTemperature:\n{temp}Â°C")
    lcd_1.refresh()
    time.sleep(5)
    
    # *** KEY CHANGE: Completely reset the display state ***
    DeactivateLCD()
    lcd_1.clear()
    lcd_1.refresh()
    time.sleep(0.2)
    ActivateLCD()
    
    # Create a new, fresh buffer for the face
    lcd_1.clear()
    
    # Draw face only, no text
    face = ChooseFace(temp, light)
    
    # Try without any previous text operations
    # Make sure we're passing the correct parameters to draw_image
    # The x=0, y=0 should explicitly set starting position
    LCD.draw_image(lcd_1, face, 84, 48, x=0, y=0)
    lcd_1.refresh()
    time.sleep(5)
    
    # Clean up
    lcd_1.clear()
    lcd_1.refresh()
    lcd_1.set_backlight(0)
    DeactivateLCD()
   

